# Build lookup dicts
_by_iso3 = {r[0]: r for r in COUNTRIES}
_by_iso2 = {r[1]: r for r in COUNTRIES}
_by_fips = {r[2]: r for r in COUNTRIES}


def iso3_to_iso2(iso3: str) -> str | None:
    row = _by_iso3.get(iso3.upper())
    return row[1] if row else None


def iso2_to_iso3(iso2: str) -> str | None:
    row = _by_iso2.get(iso2.upper())
    return row[0] if row else None


def fips_to_iso3(fips: str) -> str | None:
    row = _by_fips.get(fips.upper())
    return row[0] if row else None


def iso3_to_name(iso3: str) -> str | None:
    row = _by_iso3.get(iso3.upper())
    return row[3] if row else None


def get_all_iso3() -> list[str]:
    return [r[0] for r in COUNTRIES]
